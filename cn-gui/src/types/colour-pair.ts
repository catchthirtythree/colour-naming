export interface IColourPair {
  hex: number;
  name: string;

  getRGB(): [number, number, number];
  isColourLight(): boolean;
  toStringHex(): string;
}

export class ColourPair implements IColourPair {
  hex: number;
  name: string;

  constructor(hex: number, name: string) {
    this.hex = hex;
    this.name = name;
  }

  getRGB(): [number, number, number] {
    let r = (this.hex & 0xff0000) >> 16;
    let g = (this.hex & 0x00ff00) >> 8;
    let b = this.hex & 0xff;
    return [r, g, b];
  }

  isColourLight(): boolean {
    let [r, g, b] = this.getRGB();
    // @NOTE(michael): This is not infallible but not horrible either.
    return r > 128 && g > 128 && b > 128;
  }

  toStringHex(): string {
    return `#${this.hex.toString(16).padStart(6, '0')}`;
  }
}

